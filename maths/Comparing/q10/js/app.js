let element = [];

function createCircleq10(id) {
    const x = document.getElementById(id);

    if (element.includes(x)) {
        x.className = "q10-mark-circle";
        element.pop(x);
    } else {
        x.className = "q10-item";
        element.push(x);
    }
}

function submitAnswerq10() {
    var point1 = document.getElementById("q10input1");
    var point2 = document.getElementById("q10input2");
    var point3 = document.getElementById("q10input3");
    var point4 = document.getElementById("q10input4");
    var point5 = document.getElementById("q10input5");
    var point6 = document.getElementById("q10input6");
    var point7 = document.getElementById("q10input7");

    let isCorrect = true;

    if (
        point5.className === "q10-item" ||
        point6.className === "q10-item" ||
        point7.className === "q10-item"
    ) {
        isCorrect = false;
    } else if (
        point1.className === "q10-item" &&
        point2.className === "q10-item" &&
        point3.className === "q10-item" &&
        point4.className === "q10-item"
    ) {
        isCorrect = true;
    } else {
        isCorrect = false;
    }

    if (isCorrect) {
        openPopup();
    } else {
        let styleClasses = document.getElementById("qpanel").classList;
        styleClasses.add("incorrect-answer");

        setTimeout(() => {
            styleClasses.remove("incorrect-answer");
        }, 1000);
        reduceStarCount();
    }
}

