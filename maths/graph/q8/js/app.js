// Q8 functions start --------------------------------------------

function selectAnswerQ8(id) {
    const input = document.getElementById(id);
    var val = input.getAttribute("imValue");

    if (val == "true") {
        input.src = "../img/child_black_n_white.svg";
        input.setAttribute("imValue", "false");
    } else if (val == "false") {
        input.src = "../img/child.svg";
        input.setAttribute("imValue", "true");
    }
}

function submitAnswerQ8() {
    var correctCount = 0;
    var isCorrect;
    var mainCorrect = false;
    var input;

    for (var i = 1; i < 9; i++) {
        input = document.getElementById("q8input" + i);
        isCorrect = input.getAttribute("imValue");

        if (isCorrect == "true") {
            correctCount++;
        }
    }

    if (correctCount == 3) {
        mainCorrect = true;
    }

    if (mainCorrect) {
        openPopup();
    } else {
        let styleClasses = document.getElementById("qpanel").classList;
        styleClasses.add("incorrect-answer");

        setTimeout(() => {
            styleClasses.remove("incorrect-answer");
        }, 1000);
        reduceStarCount();
    }
}

